import UIKit

/// CGFloat + Margins
extension CGFloat {
    
    static let smallMargin: CGFloat = 4.0
    static let baseMargin: CGFloat = 12.0
    static let expandMargin: CGFloat = 20.0
    static let extraMargin: CGFloat = 32.0
}

/// CGFloat + Sizes
extension CGFloat {
    
    static let mediumSize: CGFloat = 40.0
    static let bigSize: CGFloat = 80.0
}

/// CGFloat + Radius
extension CGFloat {
    
    static let smallRadius: CGFloat = 4.0
    static let mediumRadius: CGFloat = 12.0
}
